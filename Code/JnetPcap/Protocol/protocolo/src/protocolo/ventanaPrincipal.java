                    enviarTrama(trama);
                    try{
                        bfDestino.close();
                    }catch(IOException e){
                        //
                    }
                    recibidosTxt.setText("Bytes recibidos: ");
                    recibidosTxt.setEnabled(false);
                    recibiendo = false;
                    sizeDestino = 0;
                    partesDestino = 0;
                    partesRecibidas = 0;
                    tramasEnviadas = 0;
                    macOrigen = new byte[6];
